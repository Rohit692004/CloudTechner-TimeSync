export function SidebarNav({ groups }: { groups: Group[] }) {
  const pathname = usePathname();

  return (
    <div className="flex flex-col gap-6 px-4 py-2">
      {groups.map((group) => {
        if (group.items.length === 0) return null;
        return (
          <div key={group.title} className="flex flex-col">
            <span className="px-3 text-[10px] font-bold tracking-wider text-emerald-400/50 uppercase select-none mb-1.5">
              {group.title}
            </span>
            <div className="flex flex-col gap-0.5">
              {group.items.map((item) => {
                const active =
                  pathname === item.href ||
                  (item.href !== "/" &&
                    item.href !== "/employee" &&
                    item.href !== "/manager" &&
                    item.href !== "/hr" &&
                    item.href !== "/admin" &&
                    pathname.startsWith(item.href)) ||
                  (item.href === "/employee" && pathname === "/employee") ||
                  (item.href === "/manager" && pathname.startsWith("/manager"));

                return (
                  <Link
                    key={item.href}
                    href={item.href}
                    className={cn(
                      "flex items-center justify-between px-3 py-2 rounded-lg text-sm font-medium transition-all duration-150 group",
                      active
                        ? "bg-white/10 text-white shadow-sm"
                        : "text-white/70 hover:text-white hover:bg-white/5"
                    )}
                  >
                    <div className="flex items-center gap-2.5">
                      <span className={cn(
                        "transition-colors",
                        active ? "text-emerald-400" : "text-white/40 group-hover:text-white/70"
                      )}>
                        {getIcon(item.icon)}
                      </span>
                      <span>{item.label}</span>
                    </div>
                    {item.badge !== undefined && item.badge > 0 && (
                      <span className="flex h-5 min-w-[20px] items-center justify-center rounded-full bg-emerald-500 px-1.5 text-[10px] font-bold text-white shadow-sm">
                        {item.badge}
                      </span>
                    )}
                  </Link>
                );
              })}
            </div>
          </div>
        );
      })}
    </div>
  );
}
